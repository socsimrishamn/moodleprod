// On template on change.
$('#id_template').change(function() {
    var url = $('#id_template').attr('changeurl') + $('#id_template').val();

    // Reset the value as we don't want to trigger an alert if only the select was changed by the user.
    var defaultSelectValue = $(this).find('option[selected]').val();
    $('#id_template').val(defaultSelectValue);

    window.location.replace(url);
    return false;
});
